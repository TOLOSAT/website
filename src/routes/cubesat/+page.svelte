<section>
	<h2>Le Cubesat</h2>

	<h3>Cubesat-3U</h3>
	<p>
		TOLOSAT est un nanosatellite de 3 unités. Des équipes spécifiques sont
		chargées de faire convenir le nanosatellite aux exigences électrique,
		thermique et mécanique d’une mise en orbite ainsi que du support des charges
		utiles.
	</p>
</section>
<section>
	<h2>Payloads</h2>
	<p>
		We are developing a series of payloads to be integrated into our CubeSat.
	</p>
	<h3>Iridium</h3>
	<p>
		La première charge utile scientifique vise à établir un lien bidirectionnel
		entre TOLOSAT et la constellation Iridium-Next. Ceci ayant pour but
		d’apporter plus de flexibilité aux opérations de télécommunication.
	</p>
	<h3>Gravimetry</h3>
	<p>
		La seconde mission scientifique cherche à observer les variations
		temporelles du champ de gravité terrestre. Cette solution se base sur une «
		approche accélérométrique » appuyée par le suivi GNSS.
	</p>
</section>
